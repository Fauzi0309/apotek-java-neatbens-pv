    private java.util.Date getDate(String date){
        try {
            return new SimpleDateFormat("yyyy-MM-dd").parse(date);
        } catch (ParseException ex) {
            Logger.getLogger(DataSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    
    private String getDateString(){
        SimpleDateFormat dcn = new SimpleDateFormat("yyyy-MM-dd");
        return dcn.format(tgl.getDate());
    } 
    
   
protected void datatable(){

            Object[] Baris ={"Kode PT", "Nama PT","Tanggal","Alamat","No.Telp","Email"};
                tabmode= new DefaultTableModel(null, Baris);
                String cariitem=txtcari.getText();
                
                try {
                     String sql = "SELECT * FROM supplier where kpt like '%"+cariitem+"%' "
                    + "or npt like'%"+cariitem+"%' order by kpt asc";
                    Statement stat = conn.createStatement();
                    ResultSet hasil = stat.executeQuery(sql);
                    while (hasil.next()){
                        tabmode.addRow(new Object[]{
                            hasil.getString(1),
                            hasil.getString(2),
                            hasil.getString(3),
                            hasil.getString(4),
                            hasil.getString(5),
                            hasil.getString(6),
                        });
                    }
                    tblpt.setModel (tabmode);
                } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
                }
}
