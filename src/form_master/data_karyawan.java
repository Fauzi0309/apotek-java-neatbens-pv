    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        String jenis = null; 
        if(rblakilaki.isSelected()){ 
            jenis = "Laki-Laki"; 
        }else if(rbperempuan.isSelected()){
            jenis = "Perempuan";
        }
        String sql = "update karyawan set idky=?, nama=?, TG=?, JK=?, email=?, nohp=?, agama=?, password=? where idky='"+jid.getText()+"'";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, jid.getText());
            stat.setString(2, jnama.getText());
            stat.setString(3, getDateString());
            stat.setString(4, jenis);
            stat.setString(5, jemail.getText());
            stat.setString(6, jnohp.getText());
            stat.setString(7, cbagama.getSelectedItem().toString());
            stat.setString(8, String.valueOf(jpfpass.getPassword()));
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            jid.requestFocus();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal diubah"+e);
        }
        datatable();
    }//GEN-LAST:event_beditActionPerformed

